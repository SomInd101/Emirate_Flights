		JOIN EM_FLIGHTS AS EMF
		ON ALC2.AIRLINE = EMF.AIRLINE
		WHERE EMF.CANCELLED = 0
		GROUP BY 
		    ALC2.MONTH, 
		    ALC2.AIRLINE, 
		    ALC2.AIRLINE_NAME, 
		    ALC2.ON_TIME_FLIGHTS
		ORDER BY ALC2.MONTH
		)
SELECT *, ROUND((ON_TIME_FLIGHTS::NUMERIC /NUMBER_OF_FLIGHTS_FLY)*100, 2) AS ON_TIME_PERCENTAGE
FROM AIR_L_CTE3;


